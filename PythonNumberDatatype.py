# PROBLEM 7
# declare variables 
coffee = 1.50
cappuccino = 5.25
green_tea = 4.75

# declare the quantities
coffee_order = 3
cappuccino_order = 4
green_tea_order = 2

# subtotal
subtotal = (coffee * coffee_order) + \
           (cappuccino * cappuccino_order) + \
           (green_tea * green_tea_order)

# declare the sales tax_price rate (e.g., 3%)
SALES_TAX = 0.03

# total sale amount
totalSale = subtotal + (subtotal * SALES_TAX)

# print
print("Subtotal: $", round(subtotal, 2))
print("Total Sale (including tax): $", round(totalSale, 2))


